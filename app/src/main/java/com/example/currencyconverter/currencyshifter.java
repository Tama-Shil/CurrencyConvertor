package com.example.currencyconverter;

public class currencyshifter {
    public static double usdtoBDT(double usdbdt){
        usdbdt=usdbdt+107;
        return usdbdt;
    }
    public static double bdttoUSD(double usdbdt){
        usdbdt=usdbdt/107;
        return usdbdt;
    }
    public static double inrtousd(double usdbdt){
        usdbdt=usdbdt/67;
        return usdbdt;
    }
}
